const SUPPORTED_COUNTRY_CODES = new Set<string>(
  BUSINESS_PROFILE_COUNTRIES.map(({ code }) => code)
)
const US_STATE_CODES = new Set<string>(US_STATES.map(({ code }) => code))

export function normalizeBusinessCountry(value: unknown): string | null {
  const country = String(value || "").trim().toUpperCase()
  if (!country) return null
  if (!SUPPORTED_COUNTRY_CODES.has(country)) {
    throw new Error("Business country must be a supported country code")
  }
  return country
}

export function normalizeBusinessState(value: unknown, country: string | null): string | null {
  const state = String(value || "").trim()
  if (!state) return null
  if (state.length > 120) throw new Error("Business state is too long")
  if (country === "US") {
    const stateCode = state.toUpperCase()
    if (!US_STATE_CODES.has(stateCode)) {
      throw new Error("Business state must be a valid US state code")
    }
    return stateCode
  }
  return state
}
